# Дано два списки однакової довжини. Необхідно створити з них словник таким чином, щоб елементи першого списку були
# ключами, а елементи другого відповідно значеннями нашого словника.

a = [ x for x in range(1,11)]
b = [i for i in range(1,111,10)]
result_dict = {key:  value for key, value in zip(a, b)}

print(result_dict)